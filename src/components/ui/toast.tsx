interface ToastContainerProps {
  toasts: Toast[]
  onRemove: (id: string) => void
}

function ToastContainer({ toasts, onRemove }: ToastContainerProps) {
  if (toasts.length === 0) return null

  return (
    <div className="fixed bottom-4 right-4 z-toast flex flex-col gap-2 max-w-sm w-full pointer-events-none">
      {toasts.map((toast) => (
        <ToastItem key={toast.id} toast={toast} onRemove={onRemove} />
      ))}
    </div>
  )
}

interface ToastItemProps {
  toast: Toast
  onRemove: (id: string) => void
}

function ToastItem({ toast, onRemove }: ToastItemProps) {
  const variant = toast.variant ?? 'default'

  return (
    <div
      className={cn(
        'pointer-events-auto flex items-start gap-3 rounded-lg border p-4 shadow-enterprise-lg animate-slide-in-from-right',
        variantStyles[variant]
      )}
      role="alert"
    >
      {variantIcons[variant] && (
        <div className="flex-shrink-0">{variantIcons[variant]}</div>
      )}
      <div className="flex-1 min-w-0">
        {toast.title && (
          <p className="font-medium text-foreground">{toast.title}</p>
        )}
        {toast.description && (
          <p className={cn('text-sm', toast.title ? 'mt-1' : '', 'text-muted-foreground')}>
            {toast.description}
          </p>
        )}
        {toast.action && (
          <button
            onClick={toast.action.onClick}
            className="mt-2 text-sm font-medium text-primary hover:text-primary/80"
          >
            {toast.action.label}
          </button>
        )}
      </div>
      <button
        onClick={() => onRemove(toast.id)}
        className="flex-shrink-0 rounded-md p-1 text-muted-foreground hover:text-foreground hover:bg-muted transition-colors"
        aria-label="Close"
      >
        <svg className="h-4 w-4" viewBox="0 0 20 20" fill="currentColor">
          <path fillRule="evenodd" d="M4.293 4.293a1 1 0 011.414 0L10 8.586l4.293-4.293a1 1 0 111.414 1.414L11.414 10l4.293 4.293a1 1 0 01-1.414 1.414L10 11.414l-4.293 4.293a1 1 0 01-1.414-1.414L8.586 10 4.293 5.707a1 1 0 010-1.414z" clipRule="evenodd" />
        </svg>
      </button>
    </div>
  )
}

// Helper functions for common toasts
export function toast(options: Omit<Toast, 'id'>) {
  // This needs the context, so we provide utility functions
  return options
}

toast.success = (description: string, title?: string) => ({
  title,
  description,
  variant: 'success' as const,
})

toast.error = (description: string, title?: string) => ({
  title: title || 'Error',
  description,
  variant: 'error' as const,
})

toast.warning = (description: string, title?: string) => ({
  title,
  description,
  variant: 'warning' as const,
})

toast.info = (description: string, title?: string) => ({
  title,
  description,
  variant: 'info' as const,
})

